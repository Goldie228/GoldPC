import { describe, expect, it } from 'vitest';
import { isValidPhone, normalizePhone, parsePhone, formatPhone } from './phone';

// ─── isValidPhone ───────────────────────────────────────────────────────────────

describe('isValidPhone', () => {
  it('возвращает true для валидного белорусского номера +375291234567', () => {
    expect(isValidPhone('+375291234567')).toBe(true);
  });

  it('возвращает true для номера с пробелами и скобками', () => {
    expect(isValidPhone('+375 (29) 123-45-67')).toBe(true);
  });

  it('возвращает true для номера из 7 цифр', () => {
    expect(isValidPhone('2912345')).toBe(true);
  });

  it('возвращает true для номера из 15 цифр (максимум E.164)', () => {
    expect(isValidPhone('123456789012345')).toBe(true);
  });

  it('возвращает false для номера меньше 7 цифр', () => {
    expect(isValidPhone('123456')).toBe(false);
  });

  it('возвращает false для номера больше 15 цифр', () => {
    expect(isValidPhone('1234567890123456')).toBe(false);
  });

  it('возвращает true для номера с префиксом 80', () => {
    expect(isValidPhone('80291234567')).toBe(true);
  });

  it('возвращает true для номера с +7', () => {
    expect(isValidPhone('+7291234567')).toBe(true);
  });

  it('возвращает true для пустых скобок и пробелов вокруг короткого номера', () => {
    expect(isValidPhone('(29) 123-45')).toBe(true);
  });

  it('возвращает false для пустой строки', () => {
    expect(isValidPhone('')).toBe(false);
  });

  it('возвращает true для строки только из пробелов и скобок без цифр', () => {
    expect(isValidPhone('() ')).toBe(false);
  });

  it('возвращает true для строки с буквами и минимум 7 цифр', () => {
    expect(isValidPhone('abc1234567xyz')).toBe(true);
  });
});

// ─── normalizePhone ─────────────────────────────────────────────────────────────

describe('normalizePhone', () => {
  it('удаляет пробелы', () => {
    expect(normalizePhone('+375 29 123 45 67')).toBe('375291234567');
  });

  it('удаляет скобки', () => {
    expect(normalizePhone('+375 (29) 123-45-67')).toBe('375291234567');
  });

  it('удаляет дефисы', () => {
    expect(normalizePhone('29-123-45-67')).toBe('291234567');
  });

  it('возвращает только цифры из混杂ной строки', () => {
    expect(normalizePhone('abc123def456')).toBe('123456');
  });

  it('возвращает пустую строку для строки без цифр', () => {
    expect(normalizePhone('hello')).toBe('');
  });

  it('оставляет ведущий плюс как нечисловой символ', () => {
    expect(normalizePhone('+375291234567')).toBe('375291234567');
  });

  it('возвращает пустую строку для пустого ввода', () => {
    expect(normalizePhone('')).toBe('');
  });

  it('корректно обрабатывает номер с пробелами и буквами', () => {
    expect(normalizePhone('+375 (29) abc 123')).toBe('37529123');
  });
});

// ─── parsePhone ─────────────────────────────────────────────────────────────────

describe('parsePhone', () => {
  it('возвращает digits как есть если начинается с 375', () => {
    expect(parsePhone('+375291234567')).toBe('375291234567');
  });

  it('конвертирует префикс 80 в 375', () => {
    expect(parsePhone('80291234567')).toBe('375291234567');
  });

  it('добавляет 375 к 9-значному номеру', () => {
    expect(parsePhone('291234567')).toBe('375291234567');
  });

  it('возвращает цифры как есть если не подходит ни под одно правило', () => {
    expect(parsePhone('1234567890')).toBe('1234567890');
  });

  it('корректно парсит номер с пробелами и скобками (+375)', () => {
    expect(parsePhone('+375 (29) 123-45-67')).toBe('375291234567');
  });

  it('корректно парсит номер 80 с пробелами', () => {
    expect(parsePhone('80 (29) 123-45-67')).toBe('375291234567');
  });

  it('добавляет 375 к 9-значному номеру с разделителями', () => {
    expect(parsePhone('29-123-45-67')).toBe('375291234567');
  });

  it('возвращает пустую строку для пустого ввода', () => {
    expect(parsePhone('')).toBe('');
  });

  it('handles 8-значный номер — не добавляет 375', () => {
    expect(parsePhone('29123456')).toBe('29123456');
  });

  it('handles 10-значный номер — не добавляет 375', () => {
    expect(parsePhone('2912345678')).toBe('2912345678');
  });
});

// ─── formatPhone ────────────────────────────────────────────────────────────────

describe('formatPhone', () => {
  it('форматирует полный 9-значный номер', () => {
    expect(formatPhone('291234567')).toBe('+375 (29) 123-45-67');
  });

  it('возвращает только префикс для пустой строки', () => {
    expect(formatPhone('')).toBe('+375');
  });

  it('показывает скобки после одной цифры (с пробелом)', () => {
    // Функция всегда добавляет ') ' после цифр в скобках
    expect(formatPhone('2')).toBe('+375 (2) ');
  });

  it('закрывает скобки после двух цифр (с пробелом)', () => {
    expect(formatPhone('29')).toBe('+375 (29) ');
  });

  it('добавляет цифры без дефиса до 5-й позиции (с пробелом)', () => {
    // 5 цифр: ') ' + 3 цифры, дефис добавляется т.к. length >= 5
    expect(formatPhone('29123')).toBe('+375 (29) 123-');
  });

  it('добавляет первый дефис после 5 цифр (6 цифр)', () => {
    expect(formatPhone('291234')).toBe('+375 (29) 123-4');
  });

  it('добавляет второй дефис когда length >= 7 (ровно 7 цифр)', () => {
    // 7 цифр: дефис добавляется т.к. length >= 7
    expect(formatPhone('2912345')).toBe('+375 (29) 123-45-');
  });

  it('обрезает до 9 цифр если передано больше', () => {
    expect(formatPhone('291234567890')).toBe('+375 (29) 123-45-67');
  });

  it('formatPhone удаляет нечисловые символы, берёт первые 9 цифр', () => {
    // '+375 (29) 123-45-67' → digits: '375291234567' → slice(0,9): '375291234'
    expect(formatPhone('+375 (29) 123-45-67')).toBe('+375 (37) 529-12-34');
  });

  it('корректно работает с 3 цифрами', () => {
    expect(formatPhone('291')).toBe('+375 (29) 1');
  });

  it('корректно работает с 6 цифрами', () => {
    expect(formatPhone('291234')).toBe('+375 (29) 123-4');
  });

  it('корректно работает с 8 цифрами', () => {
    expect(formatPhone('29123456')).toBe('+375 (29) 123-45-6');
  });
});
